function sourcePageUrl(title) {
  return `https://commons.wikimedia.org/wiki/${encodeURI(title.replace(/ /g, '_'))}`;
}

function commonsImage(filename, caption, alt = caption, stain) {
  const file = commonsFiles[filename];
  if (!file) {
    throw new Error(`Missing resolved Commons image source for ${filename}`);
  }

  return {
    imageUrl: file.imageUrl,
    sourcePageUrl: sourcePageUrl(file.title),
    alt,
    caption,
    ...(stain ? { stain } : {}),
    credit: 'Wikimedia Commons',
  };
}

